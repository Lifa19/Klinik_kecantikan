              <h3>Treatment Tubuh</h3>
            <p>
                HIFU treatment menjadi tindakan prosedur estetika untuk pengencangan kulit yang bersifat non-invasif (tidak perlu sayatan ke tubuh),
                serta tidak menimbulkan rasa sakit. Prosedur ini menggunakan energi ultrasonik yang mendorong produksi kolagen agar kulit terlihat lebih kencang
                Energi ultrasonik tersebut akan menargetkan jaringan kulit di bawah lapisan permukaan.
                Energi tersebut memicu jaringan kulit menjadi panas dan akan menyebabkan ‘kerusakan’ selular.
                Kerusakan inilah yang akan merangsang sel untuk menghasilkan lebih banyak kolagen.
            </p>
